export function getTableElementOffsetLeft() {
    return window.document.getElementsByTagName('table')[0].offsetLeft;
}

export function getTableElementOffsetTop() {
    return window.document.getElementsByTagName('table')[0].offsetTop;
}

export function getTableElementWidth() {
    return window.document.getElementsByTagName('table')[0].clientWidth;
}

export function getTableElementHeight() {
    return window.document.getElementsByTagName('table')[0].clientHeight;
}